<section id="main_page">
	<div class="wrap">
		<p2>
			It was 45 days ago that I took the liberty of becoming free. I set out with my
			laptop, ukulele and inline skates to cross state borders and to work with the
			music industry from anywhere in the world. Because technology gives us
			freedom.
		</p2>
		<h3>
			Places to visit before you die
		</h3>
		<p3>
			Many people want to travel the world and nearly everyone claims that
			„someday” they will actually do it. Truth is, most of us never make it past the
			pictures stored in our Pinterest folder named „Places to visit before you die”,
			because we are always looking for every reason for why this is not easy like
			that, why we could go later but not now… I was doing the same until I decided 
			to enrich my life with opportunities for making this wish a reality. And here I
			am, living my dream, going through the same Pinterest folder, only this time it’s
			titled “Places I will visit”. Not tomorrow. Today.
		</p3>
		<p3>
			I had planned this trip for more than a year. Constructively planning every
			other evening after work, I was slowly getting closer to my goal. Just as a book
			cannot be written in a day, a journey cannot be planned in a short timeframe. A
			great deal of time was spent on getting my own business up and running,
			establishing a stable clientele in Latvia and finding the right people to run the
			agency with. We’ve done a lot of experimenting and learning to work with
			freedom – that is, physically we can be anywhere in the world and still
			cooperate and work towards one goal, running our projects with the help of
			modern technology.
		</p3>
		<h3>
			How much does your life weight?
		</h3>
	</div>
	<div class="wrap_img">
		<img id="img_backpack" src="/img/backpack.gif" alt="Backpack"></img>
	</div>
	<div class="wrap">	
		<p4>
			There is this movie <a target="_blank" href="http://www.imdb.com/title/tt1193138/">“Up In The Air”</a>, based on a <a target="_blank" href="http://www.goodreads.com/book/show/30680.Up_in_the_Air">novel by Walter Kirn</a> about a
			man traveling all over the country with just a suitcase in his hand, living at
			airports and hotels and collecting the mileage he has flown. For quite some
			time now I have been dwelling on a quote from this movie, voiced by George
			Clooney (starring as Ryan Bingham).
		</p4>
		<div id="video">
