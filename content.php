		<h6>
			Our living space is not the m2 we rent – it’s the world
		</h6>
		<p7>
			Before my trip, while living in Latvia, I had moved 5 times and lived in 3 cities.
			Each occasion of moving helped me get rid of unneeded stuff and people in my
			life. For the last year I had a unique experience with trying to live in a 4 m2
			room. Most people become claustrophobic or just feel uncomfortable in a
			small space, because the world suddenly seems too confined as they are forced
			to be so close to themselves. At first I too thought this is the ultimate horror,
			but during one year of living in this err… room I learned two important lessons
			about spaciousness:
		</p7>
		<p7>
			Firstly, we own the whole world, not just the living space we rent. Even the
			most spacious penthouse apartment is nothing compared to the space of our
			world.<br>
		</p7>
		<p7>
			Secondly, it’s not the world, which is providing space, but our own inner space,
			when we’re alone, even in a very loud or cramped environment. It’s the peace
			of our mind. That’s freedom. So, no matter where I am, I’ll be calm, creative and
			happy, because, with the breath of our universe soughing through me, I shall
			have my inner space grow ever vaster.
		</p7>
		<p7>
			I can’t tell, where I’ll decide to settle down in the long term, but I already know
			it will be a small, cozy apartment of a smart and practical design of my own. I
			am sure that by then I’ll have learned to organize my life even better. Lately I’ve
			been hearing about more and more people choosing to live smaller, but
			smarter and more consciously. I am fascinated by the <a target="_blank" href="http://www.thetinylife.com/">tiny house movement</a> –
			for instance, San Francisco architects are already designing <a target="_blank" href="http://www.panoramic.com/smartspace./">smartspace
			apartment buildings</a> in full swing.
		</p7>
		<h7>
			No Travel Blog, OK?
		</h7>
		<p7>
			I have not undertaken this adventure to tick off countries and cities where I’ve
			been or to report my every move in tweets and shallow vintage-filter-processed
			pics on Instagram. I won’t be keeping a travel blog, because the thought of
			coming up with the next blog post would compromise my freedom to enjoy my
			time (I shall leave this to the other 100,000 travel blogs out there). I will just
			post an update on my Facebook timeline every now and then, letting people
			know what shows I’m planning to attend, what places we can meet up, as well
			as „Life events” with the cities I’m moving over to live and work in. And… there’s
			going to be more into the story.
		</p7>
		<h6>
			Who am I, anyway?
		</h6>
		<p7>
			My name is Jānis Vērzemnieks. I’m from Latvia (A country in Europe. Just in
			case). I’m a 23-year old music fan, graphic designer and an
			entrepreneur-freshman in music industry. To gain my financial and creative
			freedom, I started out as a freelance designer. Now I have already founded a
			design studio and music marketing agency named <a target="_blank" href="http://www.jveart.com/">JVeart</a>, though we feel more
			like a rock-band playing alternative advertising rock, design’n’code and
			branding pop. We recently issued our debut portfolio album and I am excited
			about handing a copy to the industry-people I’ll meet along the way. We’re also
			writing our second portfolio album, which is due to be out in 2015, when I’ll
			return to Latvia.
		</p7>
