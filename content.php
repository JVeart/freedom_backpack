		<p3>
			Whenever I’m moving between various lines in my life – because life consists of
			several diverse cycles – I always find it useful to ask myself: “What’s in my
			backpack?”. Most often this question crawls up to your conscious at the
			luggage scale in airports, and sometimes the bagstraps over your shoulders
			suddenly feel too tight for comfort… Those are all the things that you don’t
			need, namely, unneeded knickknacks, people, addictions, desires, habits, traits,
			fears, deadlines, unfulfillable promises, goals and dreams that are not yours…
		</p3>
		<h4>
			The “Freedom Backpack” is my mantra for a more happy and simple way of life
		</h4>
		<p7>
			Before leaving for the airport, I took everything out of my bag and the started
			putting everything back, one item at a time.<br>
			I started with the easiest - stuff.
		</p7>
		<h5>
			Clothes
		</h5>
		<p7>
			I put back only those pieces of clothing, which allow for comfort, leaving out
			the ones, which just give the looks - they only weigh down your bag. We spend
			so much time on thinking what we’ll put on for a business meeting, a BBQ
			party or a date. At the end of the day all this socializing becomes a burden,
			because 90% of the attention is devoted to ourselves – we keep on thinking
			along the lines of „Does this shirt really look good on me?” or „Do I look good
			enough?” instead of just meeting up and enjoying the moment of being
			together.
		</p7>
		<h5>
			Ukulele
		</h5>
		<p7>
			Taking an electric guitar along with all the accessories would be too much of a
			luxury, so I decided to take up the „small guitar” – the ukulele! I went for the
			<a target="_blank" href="http://www.kalabrand.com/Models/Kala/Travel/DetailsPageTravelUke.html">“Kala Thinline travel ukulele”</a>, an instrument specially designed for travelers,
			featuring a thinner body and… an inbuilt compass. 
		</p7>
		<h5>
			Inline skates
		</h5>
		<p7>
			I began inline skating just half a year ago… I had never skated before. One
			night, however, I had a dream about freedom, where I breezed through the
			nightly streets of the city, and – first thing in the morning – I went straight to a
			skate shop and got my first pair of inliners. My friends couldn’t wrap their
			heads around as to how I got this idea, but I knew from the very beginning –
			this was my sport and I would do well at it, because I had already made my
			choice. And it is a fun sport indeed! I have already learned some valuable
			lessons about the city’s architecture and the balance between body and mind. 
		</p7>
		<h5>
			Camera
		</h5>
		<p7>
			I won’t take a classic photo camera, because I won’t be keeping an eye out for
			every beautiful moment or scene. When I think about hauling a big reflex
			camera with me all the time, setting up the best shooting mode and waiting for
			the right moment, I soon realize it would only take away from enjoying the
			moment. Life is about seeing and experiencing, not capturing on film, though I
			have to admit – listing through photos after a trip is just great. So I chose to
			take a stunning little invention – the <a target="_blank" href="http://getnarrative.com/">“Narrative Clip” camera</a> – a tiny 3x3 cm
			camera that snaps a shot every 30 seconds. I keep it clipped to my bagstrap. At
			the end of the day all pics are uploaded to a cloud server and an app and, what
			is more, I’ve truly enjoyed all the captured moments without ever trying to
			capture them. These photos tell a living and true story, because all the
			moments are genuinely true, unstaged and natural.
		</p7>
		<h5>
			Data
		</h5>
		<p7>
			I upload all my files to the <a target="_blank" href="https://www.dropbox.com/">Dropbox</a> cloud service. That way I don’t have to carry
			around any GB, yet everything is instantly accessible from anywhere and with
			any device.
		</p7>
		<h5>
			Books
		</h5>
		<p7>
			My 7 mm-thin tablet can take in all my bookshelf and still has room enough for the world’s
			biggest library with 40 million units. In other words – I am subscribed to <a target="_blank" href="http://www.scribd.com/">Scribd</a>.
		</p7>
		<h5>
			Music
		</h5>
		<p7>
			I am subscribed to <a target="_blank" href="http://www.rdio.com/">Rdio</a>, so I don’t have to carry all my music collection with
			me. Believe me – it’s bigger than yours. Music is everywhere: on my laptop
			when I’m working, on my iPod when I’m skating. Rdio music stream is a true
			musical freedom revolution.
		</p7>
		<h5>
			Habits
		</h5>
		<p7>
			The next and the hardest part – addictions, desires, habits and traits. I am
			taking all of them with me, as one cannot escape or hide from these things,
			only try to. And I think this is one of the most common mistakes by travelers –
			to seek to escape their own past with the excuse of „searching for their inner
			self”.<br>
			I have taken it upon myself to lose this weight along the way and come home
			with a lighter bag than I left with. For instance – thank my sanity – it’s been half
			a year since I gave up smoking. For a passionate smoker, giving up the
			cigarette means dropping a huge weight off his shoulders and regaining
			freedom, which was taken by the nicotine in the bloodstream.
		</p7>
		<h5>
			Work
		</h5>
		<p7>
			I am taking my relationships with my clients and colleagues with me. All my
			projects and deadlines. I have pledged to myself that I will lose even more fear
			and stress.<br>
			I want to drop the small jobs and devote all my time and energy to big projects.
			I want to do something that holds some real meaning and value, that would
			leave an imprint in the sands of time.
		</p7>
		<h4>
			Riga - Tallinn - Venice - Brighton - Paris - Keflavík - Groningen - Tallinn - Riga
		</h4>
		<p7>
			During my trip I have decided to live in several countries, moving to a different
			city every 8 to 12 weeks. My trip will begin and end with the Tallinn Music Week
			showcase festival and conference. I already know I’ll fall in love with Venice,
			playing my ukulele in the warm summer nights at the Grand Canal. I’ll try out the
			rides at Brighton Pier and visit The Great Escape Showcase festival and
			conference. In Paris, I’ll breathe freedom, sweeping through the evidence of
			magnificent architecture on my inliners, and visit the MaMa festival and
			convention. Once I get to Iceland, I’ll switch off my internet access, live in the
			mountains and swim in the blue lagoon. And I intend to visit the Eurosonic
			showcase festival and conference in Groningen. However, I do reserve the right
			to change my trip plan and make some spontaneous moves, for I have the
			freedom to do so.
		</p7>
