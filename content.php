		<div id="quote" class="quote">
			Imagine for a second that you're carrying a backpack. I want you to
			pack it with all the stuff that you have in your life... you start with the
			little things. The shelves, the drawers, the knickknacks, then you start
			adding larger stuff. Clothes, tabletop appliances, lamps, your TV... the				backpack should be getting pretty heavy now. You go bigger. Your<br>
			couch, your car, your home... I want you to stuff it all into that
			backpack.
			Try to walk.
			Now I want you to fill it with people. Start with casual acquaintances,
